/* Bitfield Definitions */

/* AK4953A_00_POWER_MANAGEMENT1 (0x00) Fields */
#define AK4953A_PMVCM				0x40

/* AK4953A_01_POWER_MANAGEMENT2 (0x01) Fields */
#define AK4953A_PMPLL				0x01
#define AK4953A_M_S				0x08

/* AK4953A_05_MODE_CONTROL1 (0x05) Fields */
#define AK4953A_DIF				0x03
#define AK4953A_DIF_24MSB_24LSB_MODE		(0 << 0)
#define AK4953A_DIF_24MSB_16LSB_MODE		(1 << 0)
#define AK4953A_DIF_24MSB_MODE			(2 << 0)
#define AK4953A_DIF_I2S_MODE			(3 << 0)
#define AK4953A_BCKO				0x08

#define AK4953A_PLL				0xF0
#define AK4953A_EXT_SLAVE			0
#define AK4953A_PLL_BICK32			(2 << 4)
#define AK4953A_PLL_BICK64			(3 << 4)
#define AK4953A_PLL_11_2896MHZ			(4 << 4)
#define AK4953A_PLL_12MHZ			(6 << 4)
#define AK4953A_PLL_24MHZ			(7 << 4)
#define AK4953A_PLL_13_5MHZ			(12 << 4)
#define AK4953A_PLL_27MHZ			(13 << 4)

/* AK4953A_06_MODE_CONTROL2 (0x06) Fields */
#define AK4953A_FS				0x1F
#define AK4953A_FS_12KHZ			(0 << 0)
#define AK4953A_FS_24KHZ			(1 << 0)
#define AK4953A_FS_48KHZ			(2 << 0)
#define AK4953A_FS_96KHZ			(0x13 << 0)

